import unittest
from unittest import TestCase

from seatcret.util import find_path


class TestUtil(TestCase):

    def test_find_path(self):
        # 2호선 순환선
        self.assertEqual(find_path('1002000212', '1002000220', 0), [
            '1002000212', '1002000213', '1002000214', '1002000215',
            '1002000216', '1002000217', '1002000218', '1002000219',
            '1002000220',
        ])
        self.assertEqual(find_path('1002000240', '1002000212', 0), [
            '1002000240', '1002000241', '1002000242', '1002000243',
            '1002000201', '1002000202', '1002000203', '1002000204',
            '1002000205', '1002000206', '1002000207', '1002000208',
            '1002000209', '1002000210', '1002000211', '1002000212',
        ])
        self.assertEqual(find_path('1002000224', '1002000212', 1), [
            '1002000224', '1002000223', '1002000222', '1002000221',
            '1002000220', '1002000219', '1002000218', '1002000217',
            '1002000216', '1002000215', '1002000214', '1002000213',
            '1002000212',
        ])

        # 3호선
        self.assertEqual(find_path('1003000336', '1003000323', 0), [
            '1003000336', '1003000335', '1003000334', '1003000333',
            '1003000332', '1003000331', '1003000330', '1003000329',
            '1003000328', '1003000327', '1003000326', '1003000325',
            '1003000324', '1003000323',
        ])
        self.assertEqual(find_path('1003000336', '1003000323', 1), None)

        # 신분당선
        self.assertEqual(find_path('1077000687', '1077006811', 0), None)
        self.assertEqual(find_path('1077000687', '1077006811', 1), [
            '1077000687', '1077000688', '1077000689',
            '1077006810', '1077006811',
        ])


if __name__ == '__main__':
    unittest.main()
